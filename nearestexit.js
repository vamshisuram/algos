var nearestExit = function (maze, entrance) {
  const [ei, ej] = entrance;
  const rows = maze.length;
  const cols = maze[0].length;

  const isExit = (r, c) => {
    return r === 0 || c === 0 || r === rows - 1 || c === cols - 1;
  };

  const inbounds = (r, c) => {
    return 0 <= r && r < rows && 0 <= c && c < cols;
  };

  const isEntry = (r, c) => r === ei && c === ej;

  const q = [{ i: ei, j: ej, d: 0 }];
  const visited = new Set();
  visited.add(`${ei},${ej}`);

  while (q.length) {
    let curr = q.shift();

    // if exit point, break & return depth;
    const { i, j, d } = curr;
    if (isExit(i, j)) {
      if (!isEntry(i, j)) {
        return d;
      }
    }

    // get neighbors and add to q if not visited
    const deltas = [
      [-1, 0],
      [1, 0],
      [0, -1],
      [0, 1],
    ];
    for (let [dx, dy] of deltas) {
      if (
        inbounds(i + dx, j + dy) &&
        !visited.has(`${i + dx},${j + dy}`) &&
        maze[i + dx][j + dy] !== "+"
      ) {
        visited.add(`${i + dx},${j + dy}`);
        q.push({ i: i + dx, j: j + dy, d: d + 1 });
      }
    }
  }

  return -1;
};

